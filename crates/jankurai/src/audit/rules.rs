#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum RepairEligibility {
    AutoSafe,
    AgentAssisted,
    HumanRequired,
    NeverAuto,
}

impl RepairEligibility {
    pub fn as_str(self) -> &'static str {
        match self {
            Self::AutoSafe => "auto-safe",
            Self::AgentAssisted => "agent-assisted",
            Self::HumanRequired => "agent-assisted",
            Self::NeverAuto => "never-auto",
        }
    }

    pub fn allows_auto_pr(self) -> bool {
        matches!(self, Self::AutoSafe | Self::AgentAssisted)
    }
}

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum RepairRisk {
    Low,
    Medium,
    High,
    Critical,
}

impl RepairRisk {
    pub fn as_str(self) -> &'static str {
        match self {
            Self::Low => "low",
            Self::Medium => "medium",
            Self::High => "high",
            Self::Critical => "critical",
        }
    }

    pub fn rank(self) -> u8 {
        match self {
            Self::Low => 1,
            Self::Medium => 2,
            Self::High => 3,
            Self::Critical => 4,
        }
    }

    pub fn parse(value: &str) -> Option<Self> {
        match value {
            "low" => Some(Self::Low),
            "medium" => Some(Self::Medium),
            "high" => Some(Self::High),
            "critical" => Some(Self::Critical),
            _ => None,
        }
    }

    pub fn is_allowed_by(self, max: Self) -> bool {
        self.rank() <= max.rank()
    }
}

