const HARD_RULES: &[LanguageRule] = &[LanguageRule {
    id: "comment-hygiene.hard",
    language: Language::Comments,
    hlt_rule_id: HLT_RULE_ID,
    severity: "high",
    category: "vibe",
    lane: "fast",
    confidence: ConfidencePolicy::High,
    matcher: Matcher::NoActiveDetectors, // detection is phrase-based, not simple contains
    proof_window: ProofWindow::None,
    problem: "dangerous comment confesses unresolved security, implementation, or quality issue",
    fix: "resolve the issue described in the comment and remove the comment",
}];

const ADVISORY_RULES: &[LanguageRule] = &[LanguageRule {
    id: "comment-hygiene.advisory",
    language: Language::Comments,
    hlt_rule_id: HLT_RULE_ID,
    severity: "medium",
    category: "vibe",
    lane: "fast",
    confidence: ConfidencePolicy::Medium,
    matcher: Matcher::NoActiveDetectors,
    proof_window: ProofWindow::None,
    problem: "security-sensitive marker comment mentions a safety/security concept",
    fix: "resolve the security-sensitive TODO or document an explicit exception",
}];

pub fn catalog() -> &'static [LanguageRule] {
    static RULES: Lazy<Vec<LanguageRule>> = Lazy::new(|| {
        let mut rules = Vec::new();
        rules.extend_from_slice(HARD_RULES);
        rules.extend_from_slice(ADVISORY_RULES);
        rules
    });
    RULES.as_slice()
}

pub fn findings(ctx: &AuditContext) -> Vec<LanguageFinding> {
    sort_and_cap_findings(hard_findings(ctx), 50)
}

pub fn advisory_signals(ctx: &AuditContext) -> Vec<LanguageFinding> {
    sort_and_cap_findings(advisory_findings(ctx), 50)
}

fn hard_findings(ctx: &AuditContext) -> Vec<LanguageFinding> {
    let mut out = Vec::new();
    for file in scannable_files(ctx) {
        for (idx, line) in file.text.lines().enumerate() {
            let line_no = idx + 1;
            if let Some(comment) = extract_comment(line, &file.suffix) {
                if let Some(hit) = tier1_hit_for_comment(file, line_no, &comment) {
                    out.push(hit);
                }
            }
        }
    }
    out
}

fn advisory_findings(ctx: &AuditContext) -> Vec<LanguageFinding> {
    let mut out = Vec::new();
    for file in scannable_files(ctx) {
        for (idx, line) in file.text.lines().enumerate() {
            let line_no = idx + 1;
            if let Some(comment) = extract_comment(line, &file.suffix) {
                if let Some(hit) = tier2_hit_for_comment(file, line_no, &comment) {
                    out.push(hit);
                }
            }
        }
    }
    out
}

