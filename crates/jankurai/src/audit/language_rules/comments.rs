#[cfg(test)]
mod tests {
    use super::*;

    fn make_file(rel_path: &str, suffix: &str, text: &str) -> FileInfo {
        FileInfo {
            rel_path: rel_path.to_string(),
            name: rel_path.rsplit('/').next().unwrap_or(rel_path).to_string(),
            suffix: suffix.to_string(),
            size: text.len() as u64,
            line_count: text.lines().count(),
            text: text.to_string(),
            is_generated: false,
            is_code: true,
        }
    }

    #[test]
    fn extract_rust_line_comment() {
        assert_eq!(
            extract_comment("  // skip auth check here", ".rs"),
            Some(" skip auth check here".to_string())
        );
    }

    #[test]
    fn extract_rust_doc_comment() {
        assert_eq!(
            extract_comment("  /// documents auth flow", ".rs"),
            Some(" documents auth flow".to_string())
        );
    }

    #[test]
    fn extract_python_hash_comment() {
        assert_eq!(
            extract_comment("  # hardcoded password for dev", ".py"),
            Some(" hardcoded password for dev".to_string())
        );
    }

    #[test]
    fn extract_sql_dash_comment() {
        assert_eq!(
            extract_comment("  -- skip validation", ".sql"),
            Some(" skip validation".to_string())
        );
    }

    #[test]
    fn no_comment_in_code() {
        assert_eq!(extract_comment("  let x = skip_auth();", ".rs"), None);
    }

    #[test]
    fn inline_comment_after_code() {
        let result = extract_comment("  let x = 5; // skip auth check", ".rs");
        assert!(result.is_some());
        assert!(result.unwrap().contains("skip auth check"));
    }

    #[test]
    fn string_literal_not_extracted_as_comment() {
        assert_eq!(
            extract_comment(r#"  let msg = "skip // auth check";"#, ".rs"),
            None
        );
    }

    #[test]
    fn tier1_detects_security_bypass() {
        let file = make_file(
            "src/main.rs",
            ".rs",
            "// skip auth check for now\nfn main() {}",
        );
        let hit = tier1_hit_for_comment(&file, 1, " skip auth check for now");
        assert!(hit.is_some());
        let finding = hit.unwrap();
        assert_eq!(finding.rule_id, HLT_RULE_ID);
        assert!(finding.problem.contains("security-bypass"));
    }

    #[test]
    fn tier1_ignores_normal_comment() {
        let file = make_file(
            "src/main.rs",
            ".rs",
            "// initialize the authentication module\nfn main() {}",
        );
        let hit = tier1_hit_for_comment(&file, 1, " initialize the authentication module");
        assert!(hit.is_none());
    }

    #[test]
    fn tier2_detects_todo_with_auth() {
        let file = make_file(
            "src/main.rs",
            ".rs",
            "// TODO: fix authentication bypass\nfn main() {}",
        );
        let hit = tier2_hit_for_comment(&file, 1, " TODO: fix authentication bypass");
        assert!(hit.is_some());
    }

    #[test]
    fn tier2_ignores_todo_without_security_context() {
        let file = make_file(
            "src/main.rs",
            ".rs",
            "// TODO: refactor this loop\nfn main() {}",
        );
        let hit = tier2_hit_for_comment(&file, 1, " TODO: refactor this loop");
        assert!(hit.is_none());
    }

    #[test]
    fn non_code_extension_not_scannable() {
        let mut file = make_file("docs/auth.md", ".md", "skip auth check");
        file.is_code = false;
        assert!(!is_comment_scannable_file(&file));
    }

    #[test]
    fn test_path_not_scannable() {
        let file = make_file("tests/auth_test.rs", ".rs", "// skip auth check");
        assert!(!is_comment_scannable_file(&file));
    }

    #[test]
    fn jankurai_crate_not_scannable() {
        let file = make_file(
            "crates/jankurai/src/audit/scan.rs",
            ".rs",
            "// skip auth check",
        );
        assert!(!is_comment_scannable_file(&file));
    }
}
