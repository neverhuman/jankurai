// Detection helpers
// ---------------------------------------------------------------------------

/// Finds the phrase in the text, ensuring it is word-bounded.
/// Returns the byte index of the start of the match.
fn find_word_bounded(text: &str, phrase: &str) -> Option<usize> {
    let mut start = 0;
    while let Some(pos) = text[start..].find(phrase) {
        let abs_pos = start + pos;
        let before_ok = abs_pos == 0 || !text.as_bytes()[abs_pos - 1].is_ascii_alphanumeric();
        let after_pos = abs_pos + phrase.len();
        let after_ok =
            after_pos >= text.len() || !text.as_bytes()[after_pos].is_ascii_alphanumeric();

        if before_ok && after_ok {
            return Some(abs_pos);
        }
        start = abs_pos + 1; // advance to find next
    }
    None
}

/// Checks if the matched phrase is immediately preceded by a negation.
fn is_negated(text: &str, match_pos: usize) -> bool {
    let before = text[..match_pos].trim_end();
    let before_lower = before.to_ascii_lowercase();
    before_lower.ends_with(" not")
        || before_lower.ends_with(" don't")
        || before_lower.ends_with(" dont")
        || before_lower.ends_with(" never")
        || before_lower == "not"
        || before_lower == "don't"
        || before_lower == "dont"
        || before_lower == "never"
}

