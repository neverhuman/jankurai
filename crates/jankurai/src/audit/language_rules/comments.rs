// Detection
// ---------------------------------------------------------------------------

fn tier1_hit_for_comment(
    file: &FileInfo,
    line_no: usize,
    comment_text: &str,
) -> Option<LanguageFinding> {
    let lower = comment_text.to_ascii_lowercase();

    // Check nearby allow first
    if nearby_allow(&file.text, line_no, "comment-hygiene") {
        return None;
    }

    for &(phrase, category) in TIER1_ALL.iter() {
        if let Some(pos) = find_word_bounded(&lower, phrase) {
            // Ignore if the phrase is negated (e.g. "do not skip auth")
            if is_negated(&lower, pos) {
                continue;
            }

            let snippet = file
                .text
                .lines()
                .nth(line_no.saturating_sub(1))
                .map(|l| l.trim().chars().take(160).collect::<String>())
                .unwrap_or_default();

            return Some(LanguageFinding::new(
                HLT_RULE_ID,
                "comment-hygiene.hard",
                file.rel_path.clone(),
                Some(line_no),
                snippet.clone(),
                format!(
                    "dangerous comment detected: `{}` phrase found (category: {})",
                    phrase, category
                ),
                format!(
                    "comment confesses {} — this must be resolved before merge",
                    category.replace('-', " ")
                ),
                "resolve the underlying issue described in the comment, then remove the comment; \
                 if the comment documents a necessary tradeoff, use `jankurai:allow comment-hygiene \
                 reason=... expires=...`",
                vec![
                    format!("detector=comment-hygiene.hard"),
                    format!("category={}", category),
                    format!("phrase={}", phrase),
                    format!("path={}", file.rel_path),
                    format!("line={}", line_no),
                    format!("snippet={}", snippet),
                ],
            ));
        }
    }
    None
}

fn tier2_hit_for_comment(
    file: &FileInfo,
    line_no: usize,
    comment_text: &str,
) -> Option<LanguageFinding> {
    let lower = comment_text.to_ascii_lowercase();

    if nearby_allow(&file.text, line_no, "comment-hygiene") {
        return None;
    }

    // Check if any marker word is present (word-bounded)
    let marker = TIER2_MARKERS
        .iter()
        .find(|&&m| find_word_bounded(&lower, m).is_some());

    let marker = match marker {
        Some(m) => *m,
        None => return None,
    };

    // Check if any context noun co-occurs (word-bounded)
    let context = TIER2_CONTEXT_NOUNS
        .iter()
        .find(|&&noun| find_word_bounded(&lower, noun).is_some());

    let context_noun = match context {
        Some(n) => *n,
        None => return None,
    };

    let snippet = file
        .text
        .lines()
        .nth(line_no.saturating_sub(1))
        .map(|l| l.trim().chars().take(160).collect::<String>())
        .unwrap_or_default();

    Some(LanguageFinding::new(
        HLT_RULE_ID,
        "comment-hygiene.advisory",
        file.rel_path.clone(),
        Some(line_no),
        snippet.clone(),
        format!(
            "security-sensitive {} comment: `{}` mentions `{}`",
            marker.to_uppercase(),
            marker,
            context_noun
        ),
        format!(
            "{} marker co-occurs with security/safety term `{}`",
            marker.to_uppercase(),
            context_noun
        ),
        "resolve the security-sensitive TODO or document an explicit exception with \
         `jankurai:allow comment-hygiene reason=... expires=...`",
        vec![
            format!("detector=comment-hygiene.advisory"),
            format!("marker={}", marker),
            format!("context={}", context_noun),
            format!("path={}", file.rel_path),
            format!("line={}", line_no),
            format!("snippet={}", snippet),
        ],
    ))
}

