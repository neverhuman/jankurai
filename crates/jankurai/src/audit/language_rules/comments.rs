/// Extensions that contain source code with scannable comments.
const CODE_EXTENSIONS: &[&str] = &[
    ".rs", ".ts", ".tsx", ".js", ".jsx", ".mts", ".cts", ".py", ".sh", ".bash", ".go", ".sql",
    ".c", ".cc", ".cpp", ".cs", ".java", ".kt", ".kts", ".rb", ".swift", ".scala", ".dart", ".ex",
    ".exs", ".lua", ".php",
];

/// Try to extract the comment portion of a source line.
/// Returns `Some(comment_text)` if the line contains a comment, `None` otherwise.
fn extract_comment(line: &str, ext: &str) -> Option<String> {
    let trimmed = line.trim();
    if trimmed.is_empty() {
        return None;
    }

    match ext {
        // C-family // and /* comments
        ".rs" | ".ts" | ".tsx" | ".js" | ".jsx" | ".mts" | ".cts" | ".go" | ".c" | ".cc"
        | ".cpp" | ".cs" | ".java" | ".kt" | ".kts" | ".swift" | ".scala" | ".dart" => {
            // Full-line comment
            if trimmed.starts_with("///") || trimmed.starts_with("//!") {
                return Some(trimmed[3..].to_string());
            }
            if let Some(stripped) = trimmed.strip_prefix("//") {
                return Some(stripped.to_string());
            }
            if trimmed.starts_with("/*") {
                let inner = trimmed.trim_start_matches("/*").trim_end_matches("*/");
                return Some(inner.to_string());
            }
            if trimmed.starts_with('*') && !trimmed.starts_with("*/") {
                // Inside a block comment continuation line
                return Some(trimmed[1..].to_string());
            }
            // Inline comment after code
            if let Some(pos) = line.find("//") {
                // Make sure it's not inside a string literal (basic heuristic:
                // count unescaped quotes before the // position)
                let before = &line[..pos];
                let double_quotes = before.chars().filter(|&c| c == '"').count();
                if double_quotes % 2 == 0 {
                    return Some(line[pos + 2..].to_string());
                }
            }
            None
        }
        // Hash comments
        ".py" | ".sh" | ".bash" | ".rb" | ".ex" | ".exs" | ".lua" | ".php" => {
            if let Some(stripped) = trimmed.strip_prefix('#') {
                return Some(stripped.to_string());
            }
            // Python/Ruby also support inline #
            if let Some(pos) = line.find('#') {
                let before = &line[..pos];
                let double_quotes = before.chars().filter(|&c| c == '"').count();
                let single_quotes = before.chars().filter(|&c| c == '\'').count();
                if double_quotes % 2 == 0 && single_quotes % 2 == 0 {
                    return Some(line[pos + 1..].to_string());
                }
            }
            None
        }
        // SQL: -- comments
        ".sql" => {
            if let Some(stripped) = trimmed.strip_prefix("--") {
                return Some(stripped.to_string());
            }
            if trimmed.starts_with("/*") {
                let inner = trimmed.trim_start_matches("/*").trim_end_matches("*/");
                return Some(inner.to_string());
            }
            if let Some(pos) = line.find("--") {
                let before = &line[..pos];
                let quotes = before.chars().filter(|&c| c == '\'').count();
                if quotes % 2 == 0 {
                    return Some(line[pos + 2..].to_string());
                }
            }
            None
        }
        _ => None,
    }
}

// ---------------------------------------------------------------------------
// File filtering
// ---------------------------------------------------------------------------

fn is_comment_scannable_file(file: &FileInfo) -> bool {
    // Must be a code file extension
    if !CODE_EXTENSIONS.contains(&file.suffix.as_str()) {
        return false;
    }
    // Exclude docs, reference, tips, generated, paper, target
    if is_docs_reference_tips_or_generated(&file.rel_path) {
        return false;
    }
    // Exclude tests, fixtures, examples
    if is_dev_only_path(&file.rel_path) {
        return false;
    }
    // Exclude generated/reference via scan helpers
    if scan::is_generated_or_reference_path(&file.rel_path) {
        return false;
    }
    // Exclude Jankurai's own crates (self-audit exclusion)
    let lower = file.rel_path.to_ascii_lowercase();
    if lower.starts_with("crates/jankurai/")
        || lower.starts_with("crates/jankurai-proofbind/")
        || lower.starts_with("crates/jankurai-proofmark/")
    {
        return false;
    }
    true
}

fn scannable_files(ctx: &AuditContext) -> Vec<&FileInfo> {
    let zone_paths = crate::audit::helpers::generated_zone_suppression_paths(ctx);
    ctx.all_files
        .iter()
        .filter(|file| {
            is_comment_scannable_file(file)
                && !zone_paths
                    .iter()
                    .any(|zone| crate::audit::helpers::path_matches_prefix(&file.rel_path, zone))
        })
        .collect()
}

