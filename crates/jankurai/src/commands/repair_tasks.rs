//! Repair-task bank generation.
//!
//! The `jankurai repair-tasks` command runs the existing full audit engine over
//! a single repo and projects its findings into a structured, deduplicated
//! repair-task feed that downstream agents (Jekko/ZYAL) consume. It composes the
//! audit engine (it does not reimplement any scoring or detection logic): each
//! task is derived from already-computed report fields — copy-code classes,
//! tool-adoption opportunities, and hard structure/ownership findings — and is
//! given a stable SHA fingerprint id so the feed diffs cleanly across runs.

use crate::audit::copy_code::{CopyCodeClass, CopyCodeSeverity};
use crate::audit::{run_audit_with_options, AuditOptions};
use crate::model::{Finding, Report, ToolAdoptionItem};
use anyhow::{bail, Context, Result};
use serde::Serialize;
use sha2::{Digest, Sha256};
use std::collections::{BTreeMap, BTreeSet};
use std::path::{Path, PathBuf};

/// Stable note emitted in place of a wall-clock timestamp so the feed is
/// reproducible for downstream diffing and golden tests.
const GENERATED_AT_NOTE: &str =
    "Generated by jankurai repair-tasks; the underlying audit is deterministic and carries its own timestamp.";

/// Categories whose hard findings represent structural or ownership decay that a
/// Variety repair task should surface. These map to the audit `category` field.
const VARIETY_CATEGORIES: &[&str] = &["shape", "context", "boundary", "vibe"];

/// Arguments for the repair-tasks command.
#[derive(Debug, Clone)]
pub struct RepairTasksArgs {
    /// Repo to audit and convert into a repair-task feed.
    pub repo: PathBuf,
    /// Optional output file; when absent the feed is written to stdout.
    pub out: Option<String>,
    /// Output format: `json` (default) or `md`.
    pub format: String,
}

/// The full repair-task feed: the data contract consumed by repair agents.
#[derive(Debug, Clone, Serialize)]
pub struct RepairTaskBank {
    pub repo: String,
    pub generated_at_note: String,
    pub tasks: Vec<RepairTask>,
    pub totals: RepairTaskTotals,
}

/// One actionable repair task projected from the audit report.
#[derive(Debug, Clone, Serialize)]
pub struct RepairTask {
    pub id: String,
    pub kind: RepairTaskKind,
    pub target_paths: Vec<String>,
    pub instances: usize,
    pub rationale: String,
    pub suggested_action: String,
    pub est_effort: String,
    pub score_delta_if_fixed: i32,
    pub finding_fingerprints: Vec<String>,
}

/// The category of repair work a task represents.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize)]
#[serde(rename_all = "snake_case")]
pub enum RepairTaskKind {
    Dedup,
    Variety,
    ToolAdoption,
}

impl RepairTaskKind {
    /// Stable lowercase token used in the id hash and markdown rendering.
    fn token(self) -> &'static str {
        match self {
            RepairTaskKind::Dedup => "dedup",
            RepairTaskKind::Variety => "variety",
            RepairTaskKind::ToolAdoption => "tool_adoption",
        }
    }
}

/// Aggregate counts across the feed.
#[derive(Debug, Clone, Default, Serialize)]
pub struct RepairTaskTotals {
    pub task_count: usize,
    pub dedup_tasks: usize,
    pub variety_tasks: usize,
    pub tool_adoption_tasks: usize,
    pub total_instances: usize,
    pub total_score_delta_if_fixed: i32,
}

/// Entry point for `jankurai repair-tasks`.
pub fn run(args: RepairTasksArgs) -> Result<()> {
    if !args.repo.exists() {
        bail!("repo path does not exist: {}", args.repo.display());
    }
    let report = audit_repo(&args.repo)?;
    let repo_label = display_path(&args.repo);
    let bank = build_task_bank(&repo_label, &report);

    let rendered = match args.format.as_str() {
        "md" => render_markdown(&bank),
        "json" => serde_json::to_string_pretty(&bank)?,
        other => bail!("unknown format '{other}'; expected 'json' or 'md'"),
    };

    emit(&args.out, &rendered)
}

/// Run the existing full audit engine over a single repo.
fn audit_repo(repo: &Path) -> Result<Report> {
    run_audit_with_options(repo, &[], AuditOptions::default())
        .with_context(|| format!("audit {}", repo.display()))
}

/// Project a `Report` into a deduplicated repair-task bank. All detection and
/// scoring lives in the audit engine; this only shapes existing report fields.
fn build_task_bank(repo: &str, report: &Report) -> RepairTaskBank {
    let mut tasks = Vec::new();
    tasks.extend(dedup_tasks(repo, report));
    tasks.extend(variety_tasks(repo, report));
    tasks.extend(tool_adoption_tasks(repo, report));

    // Stable, deterministic order: kind token, then id.
    tasks.sort_by(|a, b| {
        a.kind
            .token()
            .cmp(b.kind.token())
            .then_with(|| a.id.cmp(&b.id))
    });

    let totals = aggregate_totals(&tasks);
    RepairTaskBank {
        repo: repo.to_string(),
        generated_at_note: GENERATED_AT_NOTE.to_string(),
        tasks,
        totals,
    }
}

/// Build Dedup tasks from copy-code classes, merging classes that target the
/// same set of paths so a single repair touches one logical duplication site.
fn dedup_tasks(repo: &str, report: &Report) -> Vec<RepairTask> {
    let classes: &[CopyCodeClass] = match report.copy_code.as_ref() {
        Some(cc) => &cc.classes,
        None => return Vec::new(),
    };

    // Group classes by their sorted, deduplicated target-path set.
    let mut groups: BTreeMap<Vec<String>, Vec<&CopyCodeClass>> = BTreeMap::new();
    for class in classes {
        let paths = class_target_paths(class);
        if paths.is_empty() {
            continue;
        }
        groups.entry(paths).or_default().push(class);
    }

    groups
        .into_iter()
        .map(|(paths, members)| {
            let instances: usize = members.iter().map(|c| c.instance_count).sum();
            let redundant_lines: usize = members.iter().map(|c| c.total_redundant_lines).sum();
            let has_hard = members
                .iter()
                .any(|c| c.effective_severity == CopyCodeSeverity::Hard || c.hard_fail);
            let fingerprints = sorted_unique(members.iter().map(|c| c.fingerprint.clone()));

            let rationale = format!(
                "{} copy-code class(es) duplicate ~{} redundant line(s) across {} path(s); duplication raises entropy and breaks single-source-of-truth.",
                members.len(),
                redundant_lines,
                paths.len()
            );
            RepairTask {
                id: task_id(repo, RepairTaskKind::Dedup, &paths),
                kind: RepairTaskKind::Dedup,
                target_paths: paths,
                instances,
                rationale,
                suggested_action:
                    "Extract the duplicated unit into a single shared definition and replace each copy with a reference."
                        .to_string(),
                est_effort: effort_for(instances, has_hard),
                score_delta_if_fixed: if has_hard { 4 } else { 2 },
                finding_fingerprints: fingerprints,
            }
        })
        .collect()
}

/// Build Variety tasks from hard structure/ownership findings, one task per
/// affected path (collapsing all hard findings on that path into one task).
fn variety_tasks(repo: &str, report: &Report) -> Vec<RepairTask> {
    let mut by_path: BTreeMap<String, Vec<&Finding>> = BTreeMap::new();
    for finding in &report.findings {
        if finding.hardness != "hard" {
            continue;
        }
        if !VARIETY_CATEGORIES.contains(&finding.category.as_str()) {
            continue;
        }
        by_path
            .entry(finding.path.clone())
            .or_default()
            .push(finding);
    }

    by_path
        .into_iter()
        .map(|(path, findings)| {
            let paths = vec![path.clone()];
            let fingerprints = sorted_unique(findings.iter().map(|f| f.fingerprint.clone()));
            let categories =
                sorted_unique(findings.iter().map(|f| f.category.clone())).join(", ");
            let rationale = format!(
                "{} hard {} finding(s) at {} indicate structural or ownership decay that lowers semantic variety.",
                findings.len(),
                categories,
                path
            );
            let suggested_action = findings
                .iter()
                .find(|f| !f.agent_fix.is_empty())
                .map(|f| f.agent_fix.clone())
                .unwrap_or_else(|| {
                    "Split the affected code into smaller, well-owned semantic units with focused tests."
                        .to_string()
                });
            RepairTask {
                id: task_id(repo, RepairTaskKind::Variety, &paths),
                kind: RepairTaskKind::Variety,
                target_paths: paths,
                instances: findings.len(),
                rationale,
                suggested_action,
                est_effort: effort_for(findings.len(), true),
                score_delta_if_fixed: 3 * findings.len() as i32,
                finding_fingerprints: fingerprints,
            }
        })
        .collect()
}

/// Build ToolAdoption tasks from applicable tool-adoption items that have not yet
/// reached CI-backed evidence (status `missing` or `configured`).
fn tool_adoption_tasks(repo: &str, report: &Report) -> Vec<RepairTask> {
    report
        .tool_adoption
        .items
        .iter()
        .filter(|item| applicable_opportunity(item))
        .map(|item| {
            let paths = tool_adoption_paths(item);
            let rationale = format!(
                "Jankurai-backed tool '{}' is applicable but status is '{}'; adopting it replaces ad-hoc CI and adds artifact evidence.",
                item.id, item.status
            );
            let suggested_action = tool_adoption_action(item);
            RepairTask {
                id: task_id(repo, RepairTaskKind::ToolAdoption, &paths),
                kind: RepairTaskKind::ToolAdoption,
                target_paths: paths,
                instances: 1,
                rationale,
                suggested_action,
                est_effort: if item.status == "configured" {
                    "S".to_string()
                } else {
                    "M".to_string()
                },
                score_delta_if_fixed: if item.status == "configured" { 1 } else { 2 },
                finding_fingerprints: Vec::new(),
            }
        })
        .collect()
}

/// A tool-adoption opportunity is an applicable tool not yet CI-backed.
fn applicable_opportunity(item: &ToolAdoptionItem) -> bool {
    item.applicable && matches!(item.status.as_str(), "missing" | "configured")
}

/// Target paths for a tool-adoption task: its declared artifact paths, or a
/// synthetic control-plane marker keyed by tool id so the id stays stable.
fn tool_adoption_paths(item: &ToolAdoptionItem) -> Vec<String> {
    if item.artifact_paths.is_empty() {
        vec![format!("tool-adoption:{}", item.id)]
    } else {
        sorted_unique(item.artifact_paths.iter().cloned())
    }
}

/// Suggested action for a tool-adoption task: prefer the CI command, then the
/// local command, then a generic adoption instruction.
fn tool_adoption_action(item: &ToolAdoptionItem) -> String {
    if let Some(ci) = item.ci_command.as_ref().filter(|s| !s.is_empty()) {
        format!("Wire '{}' into CI: {}", item.id, ci)
    } else if let Some(local) = item.local_command.as_ref().filter(|s| !s.is_empty()) {
        format!("Adopt '{}' locally and in CI: {}", item.id, local)
    } else {
        format!(
            "Adopt the '{}' tool and upload its expected artifact evidence in CI.",
            item.id
        )
    }
}

/// Collect, dedup, and sort the instance paths of a copy-code class.
fn class_target_paths(class: &CopyCodeClass) -> Vec<String> {
    sorted_unique(class.instances.iter().map(|inst| inst.path.clone()))
}

/// A coarse effort estimate keyed by instance count and severity.
fn effort_for(instances: usize, hard: bool) -> String {
    match (instances, hard) {
        (0..=1, false) => "S",
        (0..=1, true) => "M",
        (2..=3, _) => "M",
        _ => "L",
    }
    .to_string()
}

/// Stable task id: sha256 over repo, kind token, and sorted target paths.
fn task_id(repo: &str, kind: RepairTaskKind, sorted_paths: &[String]) -> String {
    let mut hasher = Sha256::new();
    hasher.update(repo.as_bytes());
    hasher.update(b"\0");
    hasher.update(kind.token().as_bytes());
    for path in sorted_paths {
        hasher.update(b"\0");
        hasher.update(path.as_bytes());
    }
    format!("sha256:{:x}", hasher.finalize())
}

/// Sort and dedup an iterator of strings via a BTreeSet for determinism.
fn sorted_unique<I: IntoIterator<Item = String>>(items: I) -> Vec<String> {
    items
        .into_iter()
        .collect::<BTreeSet<_>>()
        .into_iter()
        .collect()
}

/// Compute aggregate counts from the projected tasks.
fn aggregate_totals(tasks: &[RepairTask]) -> RepairTaskTotals {
    let mut totals = RepairTaskTotals {
        task_count: tasks.len(),
        ..RepairTaskTotals::default()
    };
    for task in tasks {
        match task.kind {
            RepairTaskKind::Dedup => totals.dedup_tasks += 1,
            RepairTaskKind::Variety => totals.variety_tasks += 1,
            RepairTaskKind::ToolAdoption => totals.tool_adoption_tasks += 1,
        }
        totals.total_instances += task.instances;
        totals.total_score_delta_if_fixed += task.score_delta_if_fixed;
    }
    totals
}

/// Render the bank as a sentence-case Markdown table.
fn render_markdown(bank: &RepairTaskBank) -> String {
    use std::fmt::Write;
    let mut out = String::new();
    let _ = writeln!(out, "# jankurai repair-task bank");
    let _ = writeln!(out);
    let _ = writeln!(out, "{}", bank.generated_at_note);
    let _ = writeln!(out);
    let _ = writeln!(out, "Repo: {}", bank.repo);
    let _ = writeln!(out);
    let _ = writeln!(
        out,
        "| Id | Kind | Targets | Instances | Effort | Score delta | Action |"
    );
    let _ = writeln!(out, "| --- | --- | --- | ---: | --- | ---: | --- |");
    for task in &bank.tasks {
        let targets = task
            .target_paths
            .iter()
            .map(|p| p.replace('|', "\\|"))
            .collect::<Vec<_>>()
            .join("; ");
        let _ = writeln!(
            out,
            "| {} | {} | {} | {} | {} | {} | {} |",
            short_id(&task.id),
            task.kind.token(),
            targets,
            task.instances,
            task.est_effort,
            task.score_delta_if_fixed,
            task.suggested_action.replace('|', "\\|")
        );
    }
    let _ = writeln!(out);
    let _ = writeln!(out, "## Totals");
    let _ = writeln!(out);
    let _ = writeln!(out, "- Tasks: {}", bank.totals.task_count);
    let _ = writeln!(out, "- Dedup tasks: {}", bank.totals.dedup_tasks);
    let _ = writeln!(out, "- Variety tasks: {}", bank.totals.variety_tasks);
    let _ = writeln!(
        out,
        "- Tool adoption tasks: {}",
        bank.totals.tool_adoption_tasks
    );
    let _ = writeln!(out, "- Total instances: {}", bank.totals.total_instances);
    let _ = writeln!(
        out,
        "- Total score delta if fixed: {}",
        bank.totals.total_score_delta_if_fixed
    );
    out
}

/// Short id form for the markdown table: keep the sha prefix readable.
fn short_id(id: &str) -> String {
    id.strip_prefix("sha256:")
        .map(|hex| format!("sha256:{}", &hex[..hex.len().min(12)]))
        .unwrap_or_else(|| id.to_string())
}

/// Write the rendered feed to the chosen sink (stdout or a file).
fn emit(out: &Option<String>, rendered: &str) -> Result<()> {
    match out {
        Some(path) => crate::render::write_json(path, rendered),
        None => {
            println!("{rendered}");
            Ok(())
        }
    }
}

fn display_path(repo: &Path) -> String {
    repo.to_string_lossy().into_owned()
}

